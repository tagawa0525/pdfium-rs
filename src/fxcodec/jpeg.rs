use crate::error::Result;

/// Decode a JPEG byte stream into raw RGB or grayscale pixels.
///
/// Returns `(pixels, width, height, color_space)` where:
/// - `pixels` is raw decoded bytes (1 byte/pixel for gray, 3 bytes/pixel for RGB)
/// - `color_space` is the JPEG color space reported by the decoder
pub fn decode(data: &[u8]) -> Result<DecodedJpeg> {
    let _ = data;
    unimplemented!("jpeg decode not yet implemented")
}

/// Result of a JPEG decode operation.
pub struct DecodedJpeg {
    /// Raw pixel data (gray: 1 byte/pixel, RGB: 3 bytes/pixel).
    pub pixels: Vec<u8>,
    pub width: u32,
    pub height: u32,
    pub color_space: JpegColorSpace,
}

/// Color space reported by the JPEG decoder.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum JpegColorSpace {
    Grayscale,
    Rgb,
    Cmyk,
}

#[cfg(test)]
mod tests {
    use super::*;

    /// Minimal valid 1×1 white grayscale JPEG.
    ///
    /// Components: SOI, JFIF APP0, DQT (all-1 table), SOF0 (1×1 gray),
    /// DHT DC0, DHT AC0, SOS, scan data (white), EOI.
    const GRAY_1X1_JPEG: &[u8] = &[
        0xFF, 0xD8, // SOI
        0xFF, 0xE0, 0x00, 0x10, 0x4A, 0x46, 0x49, 0x46, 0x00, 0x01, 0x01, 0x00, 0x00, 0x01, 0x00,
        0x01, 0x00, 0x00, // APP0 JFIF
        0xFF, 0xDB, 0x00, 0x43, 0x00, // DQT (64 bytes, all 1)
        0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01,
        0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01,
        0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01,
        0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01,
        0x01, 0x01, 0x01, 0x01, // 64 bytes
        0xFF, 0xC0, 0x00, 0x0B, 0x08, 0x00, 0x01, 0x00, 0x01, 0x01, 0x01, 0x11,
        0x00, // SOF0: 1×1 grayscale
        0xFF, 0xC4, 0x00, 0x1F, 0x00, // DHT DC0
        0x00, 0x01, 0x05, 0x01, 0x01, 0x01, 0x01, 0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0xFF, 0xC4,
        0x00, 0x31, 0x10, // DHT AC0 (minimal: just EOB)
        0x00, 0x02, 0x01, 0x03, 0x03, 0x02, 0x04, 0x03, 0x05, 0x05, 0x04, 0x04, 0x00, 0x00, 0x01,
        0x7D, 0x01, 0x02, 0x03, 0x00, 0x04, 0x11, 0x05, 0x12, 0x21, 0x31, 0x41, 0x06, 0x13, 0x51,
        0x61, 0x07, 0x22, 0x71, 0x14, 0x32, 0x81, 0x91, 0xA1, 0x08, 0x23, 0x42, 0xB1, 0xC1, 0x15,
        0x52, 0xD1, 0xF0, 0xFF, 0xDA, 0x00, 0x08, 0x01, 0x01, 0x00, 0x00, 0x3F, 0x00, // SOS
        0x7F, 0xA4, // scan data (1×1 white)
        0xFF, 0xD9, // EOI
    ];

    #[test]
    #[ignore = "not yet implemented"]
    fn decode_gray_1x1_jpeg() {
        let result = decode(GRAY_1X1_JPEG).unwrap();
        assert_eq!(result.width, 1);
        assert_eq!(result.height, 1);
        assert_eq!(result.color_space, JpegColorSpace::Grayscale);
        assert_eq!(result.pixels.len(), 1);
        // White pixel: value near 255
        assert!(
            result.pixels[0] > 200,
            "expected white pixel, got {}",
            result.pixels[0]
        );
    }

    #[test]
    #[ignore = "not yet implemented"]
    fn decode_invalid_data_is_error() {
        let result = decode(b"not a jpeg");
        assert!(result.is_err());
    }
}
